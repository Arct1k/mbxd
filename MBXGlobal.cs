    public class MBXString
    {
        private byte length;
        private string str;

        public MBXString(byte[] data)
        {
            this.length = data[0];
            this.str = System.Text.Encoding.ASCII.GetString(data, 1, this.length);
        }

        public MBXString(byte[] data, uint offset)
        {
            this.length = data[offset];
            this.str = System.Text.Encoding.ASCII.GetString(data, (int)offset+1, this.length);
        }

        public byte GetLength()
        {
            return this.length;
        }

        public override string ToString()
        {
            return this.str;
        }
    }
