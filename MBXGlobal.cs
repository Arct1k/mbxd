            if ((number >= 0) && (number < 0x103))
            {
                return function[number];
            }
            else if ((number >= 0x401) && (number < 0xA0F))
            {
                return function2[number - 0x401];
            }
            else
            {
                return "Unknown_function_0x" + (number).ToString("X4"); ;
            }
        }
